                                <h2 class="title"></h2>
                                <p>一例を挙げましょう。「プロスタイル札幌 宮の森」の外壁には丸太が並んでいます。小径木（しょうけいぼく）と呼ばれる、大きく育つ前の段階の細い木です。</p>
                                <p>「木は素材として気持ちがいいだけではなく、木を使うことで周辺環境の循環に役立ちます」と、隈氏は木と環境の関係を説明します。</p>
                                <h4>「これ、すごく重要なことです。コンクリートや鉄は環境の循環とは無関係ですが、木は僕らの国土、日本という国の環境循環にとって非常に重要な位置を占めています。そこを意識しているから僕は木を使う。さわり心地がいいとか、見た目がいいというのはもちろんありますが、それ以上の意味が木にはあります。定期的に木を切って植えることで森林の状態を良くし、洪水を減らすことができ、花粉症を減らすことができます。そういうことも含めての『木』なのです」</h4>
                            </div>
                            <div class="box_born_img">
                                <img src="https://www.prostyle-residence.com/sapporo/miyanomori/wp-content/themes/miyanomori/assets/images/main/message-3D.jpg" alt="message-3D" class="img-fluid">
                            </div>
                            <div class="box_born">
                                <h2 class="title">「北海道産」であることに意義がある </h2>
                                <p>幸運なことに、「プロスタイル札幌 宮の森」に使う間伐材は道南杉です。つまり、北海道産。</p>
                                <h4>「その場所の木を使うことが二酸化炭素（CO2）吸収の観点から一番いい。木材を輸送するときに二酸化炭素を出してはいけませんから。それが理想ですが、今回のケースのように実現できるケースは非常に少ないのが実状です。北海道の木が使えたのは、とても貴重なことです。今回は、伐採した丸太の三面を切り落とし、その材を並べてパネル状にしたものをコンクリートの型枠として使用しています。以前からこの材料には興味があったのですが、なかなか使うチャンスがありませんでした。今回初めて、『プロスタイル札幌 宮の森』で実現します」</h4>
                            </div>
                            <div class="box_born_img">
                                <div class="box_images">
                                    <img src="https://www.prostyle-residence.com/sapporo/miyanomori/wp-content/themes/miyanomori/assets/images/main/message-3E.jpg" alt="message-3E" class="img-fluid">
                                    <span>2022年9月撮影</span>
