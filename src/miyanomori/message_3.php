                            <div class="box_born">
                                <h2 class="title"></h2>
                                <p>完成した状態を示すパースは、1本1本表情の異なる道南杉が、建物に豊かな表情を与えていることを表しています。隈氏は「板にはない、厚みがあって迫力が感じられる建物になる」と、出来映えに太鼓判を押します。</p>
                                <p>通常、丸太の状態で切り出した木は、製材して薄く切りそろえ板にして使います。「プロスタイル札幌 宮の森」の場合はそうではなく、自然のままの状態で使うことに特色があります。</p>
                                <h4>「ふぞろいなことに価値がある。それがこれからの美学になると思っています。20世紀は大量生産の時代だから、全部同じであることが価値でした。そのため、木材も規格化が進みました。でも実際には、自然の素材はひとつ一つ顔が違う。逆にふぞろいであることが価値なんだというのが、これからの時代です。そういうアピールもしていきたいですね」</h4>
                            </div>
                            <div class="box_born_img">
                                <div class="box_images">
                                    <img src="https://www.prostyle-residence.com/sapporo/miyanomori/wp-content/themes/miyanomori/assets/images/main/message-3F.jpg" alt="message-3F" class="img-fluid">
                                    <span>2022年9月撮影</span>
