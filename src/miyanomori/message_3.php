                            <div class="box_born">
                                <h2 class="title">木と一緒に人生を豊かに過ごしてもらいたい</h2>
                                <p>20世紀の建築はコンクリートと鉄で競争が行われていました。「21世紀は木の競争が始まっている」と、世界で活躍する隈氏は語ります。大昔の日本は木を使うことに長けており、リーダー的な存在だったが、現在は遅れているといいます。だから海外の方が木や北海道の魅力に日本人よりも先に気が付いてしまう。その遅れを取り戻し、再び世界をリードしたい、そして日本の方に世界的な流れを気が付いて欲しいというのが、隈氏の思い。だから、「木の建物を日本で作りたい」という強い思いを抱くようになったといいます。その最新事例が、「プロスタイル札幌 宮の森」です。</p>
                                <h4>「木は切ったときにその命が終わるのではなく、ずっと生き続ける素材です。生き続けるから経年変化する。そういう素材は人間にとってものすごく親しみが持てるし、友だちのような感覚になれるはずです。『プロスタイル札幌 宮の森』に住まう方も、木と一緒に人生を豊かに過ごしていただきたいと思いますね」</h4>
                                <p>「プロスタイル札幌 宮の森」を彩る木は、建築材料としての木ではなく、環境を守るための木でできています。しかも、親しみやすく、変化も楽しめる。世界をリードする気概と価値が込められた、新しい木の使い方が特徴です。</p>
