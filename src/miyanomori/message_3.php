                                <h2 class="title">ヨーロッパではすでに始まっている</h2>
                                <h4>「この間、スキージャンプ大会を見てきたばかりです。ウインタースポーツのメッカが街のすぐ脇の丘の上にあるなんて、世界を見渡してもこんなに恵まれた場所はありません。その価値を最大限味わっていただきたいですね」</h4>
                                <p>そう語るのは、世界的な建築家、隈研吾氏。隈氏は2022年夏に宮の森に竣工する「プロスタイル札幌 宮の森」を設計・デザイン・監修をしました。このラグジュアリー・レジデンスは、「木」の印象が全体を覆いつつ、寄り添うように集まり、地形に溶け込むようにして存在するのが特徴です。隈氏にとって新境地を拓くことになるこの集合住宅に対し、「環境に対する意識が高い人に住んでもらいたいですね」と想いを語ります。</p>
                                <h4>「ヨーロッパでは、建物全体が木でできた集合住宅が出来始めています。そういう住宅に住んでいる人は、環境に対する意識が高い方が多い。日本ではまだそういう集合住宅は少ないので、『プロスタイル札幌 宮の森』がその先駆けになったらいいなと思っています。木を使ったこの建物は見た目に温かいので、そこに惹きつけられる方や価値を感じる方もすでにいるでしょう。環境の観点でこの建物を構成している要素を知っていただければ、もっとこの建物に対する理解が深まると思います」</h4>
