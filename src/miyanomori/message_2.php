                                <h2 class="title"></h2>
                                <p>「プロスタイル札幌 宮の森」の完成予想図を見ると、緑豊かな丘に、総戸数20戸を構成する建物が佇んでいるのがわかります。建物の構造は鉄筋コンクリート造一部鉄骨造ですが、外壁にあしらわれた木の印象が強く、ゆえにやさしく、温かく感じます。</p>
                                <p>丘のような建築にしたことに留まらず、周囲に溶け込むような建築としたことも、 “挑戦”の一部だと隈氏は言います。</p>
                                <h4>「20世紀の建築は地面から離れる建築でした。最終的には超高層ビルに行き着きます。これからは逆です。建築がどんどん自然の中に溶けていく時代が来ると思っています。20世紀の建築と逆で、これからの建築は自然に還っていく。コロナ禍によって、超高層型の箱に閉じ込められて働いたり、住んだりすることはすごく密で不自然に感じられるようになってきました。これからは、どう自然に還っていくかが重要なテーマになっていくでしょう。コロナ禍がその流れを加速させています。その意味で、地形に溶け込んでいく『プロスタイル札幌 宮の森』は、新しい時代のリーダーになる建築だと思います」</h4>
                            </div>
                            <div class="box_born_img">
