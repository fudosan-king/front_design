                                <h2 class="title">集合住宅のスタンダードを壊すきっかけに…</h2>
                                <p>集合住宅というと大きな箱のような建築が一般的ですが、「プロスタイル札幌 宮の森」は違います。一見すると小さな箱が無秩序に散らばっているようでいて、その実、適度な均衡を保って丘の斜面に寄り添うようにまとまっています。これも、隈氏の新しい試みのひとつです。</p>
                                <h4>「この建築は、集合住宅の形態としても新しくて、地形と同化しているという言い方もできるし、ひとつの村のような建築という言い方もできます。従来のマンションや集合住宅のスタンダードを壊すきっかけになると思っています」</h4>
                                <p>「プロスタイル札幌 宮の森」はひとつ一つの住戸が集積することで、ひとつの大きな家のような集合住宅となっています。個々の住戸が、懐かしさを感じさせる路地のような屋外廊下で結ばれているのも特徴です。これは、「クルマを前提にしていない昔の道」をイメージしたものだと隈氏は説明します。クルマを前提にしていないということは、言い換えればヒューマンスケールです。</p>
                            </div>
                            <div class="box_born_img">
                                <div class="box_images">
                                    <img src="https://www.prostyle-residence.com/sapporo/miyanomori/wp-content/themes/miyanomori/assets/images/1x/message-2-1E.jpg" alt="message-2-1E" class="img-fluid">
