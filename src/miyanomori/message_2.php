                                <h2 class="title"></h2>
                                <p>室内では「要所で木をシンボリックに使っている」のがポイントだと、隈氏は見どころを教えてくれます。大開口を持つ窓のサッシは、「木サッシ」を採用しています。木は見た目に柔らかいだけでなく断熱性が高いため、「開け閉めするときに冷たい思いをしなくていい」のもポイント。「これからはどんどん木サッシの時代になっていく。その先駆けにもなるでしょう」と隈氏は説明します。</p>
                                <p>近年では大規模な公共建築のイメージが強い隈氏はそもそもなぜ、久しぶりに集合住宅を手がける気になったのでしょうか。その答えは「プロスタイル札幌 宮の森」の立地にありました。</p>
                                <h4>「場所がものすごく大きいですね。僕が建築を引き受けるかどうかは、基本的には場所なんです。本当は集合住宅をもっとやりたいがやりたいと思える場所での依頼が少ない。建築によって場所を引き立てたいと思っていますし、逆に場所が良ければ建築の価値がアップします。日本には借景という言葉がありますが、宮の森の中の第一種低層地域の限られた場所は、札幌一番の高級住宅街と言われるだけあって場所が一際良く、その場所の価値が建築に乗り移ると感じました。再開発などで造りこまれた場所ではなく、以前からの歴史もあり、今も良い住宅地に住みたいと感じる方が増える時代に入ってきています。そう言った住宅地には、守られ残されている緑や自然が近くにあることが多く、都市化が進む世界的な流れです。コロナ禍もあり、日本でもこの世界的な価値を知っている方が増えてきています。駅前や繁華街など中心地で生活しながらも車生活を送られている方には特にそのような方が多いと感じています。ただし、建築家が作るものはわざとらしさが勝ってはいけないと思っています。あくまで、地形のおもしろさが勝つべきです」</h4>
                                <p>土地や場所が持つ価値が建築を引き立てる。時代の最先端をいく「木」を使い、地形に溶け込んでいくヒューマンスケールのラグジュアリー・レジデンスが、「プロスタイル札幌 宮の森」です。</p>
