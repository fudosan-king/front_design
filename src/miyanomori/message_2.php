                                <h2 class="title">コロナ禍が加速させた新しい建築スタイル</h2>
                                <h4>「木の技術に関して、かつての日本は世界の先端を走っていました。歴史を振り返ってみると、日本人は何千年にも渡って木のリーダーであり続けたと僕は思っています。21世紀に入って木の建築が世界中で復活してきていますが、日本はむしろ後発です。そのなかで主導的な役割を果たしていきたいと考えています」</h4>
                                <p>隈氏は、木に対する関心は世界規模で高まっていると語ります。世界の最先端を歩む隈氏が手がける、最先端の木の建築がこの「プロスタイル札幌 宮の森」。宮の森に誕生するこの集合住宅は隈氏にとっても新たなる「挑戦」になるようです。</p>
                                <h4>「木を使って丘のような建築を作りたいというのが、今までにない僕の挑戦です。今までは建築の材料として木を扱ってきました。今回は木の建築であると同時に“丘のような建築”としていますし新境地です。札幌市内と自然を一望に見渡す丘で、建築と地面が一体化し、建築自体が地形になるようなことをやりたいと思いました」</h4>
