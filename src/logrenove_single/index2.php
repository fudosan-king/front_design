                                                </div>
                                                <div style="height:20px" aria-hidden="true" class="wp-block-spacer"></div>

                                                <p>「今のタワーマンションから引っ越すことには抵抗がない」なら、「売却」するよりも「賃貸」で他人に貸すことで、毎月剰余金を出し、その剰余金で消費者金融の返済に回せるはず。そのタワーマンションだと家賃18万円前後は見込めるので、住宅ローンの返済を13万円くらいに設定できれば、毎月の賃料収入から毎月の住宅ローンの返済額を差し引きして、5万円ほどの剰余金を出すことができます。住宅ローンの返済ができて、なおかつ残りの剰余金で消費者金融の借り入れ分の返済も可能。<br><br>取り急ぎ、タワーマンションを借りてくれる人を募集したところ、すぐに見つかり、その賃料収入で住宅ローンの返済と消費者金融の返済をしていくことで、毎月のキャッシュフローはクリアできます。</p>
                                                <p><strong>「『賃貸にする』という提案を呑んでいただけたことが“吉”へと転じたケースです。『任意売却』をしていたら、剰余金も捻り出せず、消費者金融の毎月返済分は自分のサラリーから返済し続けなければいけなかったため、西本様には非常に喜んでもらえました」（永井さん）</strong></p>
                                                <div style="height:20px" aria-hidden="true" class="wp-block-spacer"></div>
                                                <div style="height:20px" aria-hidden="true" class="wp-block-spacer"></div>
                                                <div style="background: #ffffff; padding: 15px; border: 1px solid #000000; border-radius: 0px; word-break: break-all;">
                                                    <p><strong><span style="font-size: 14pt;">［ケース3：安藤さん（仮名）の悩み<br>（47歳・機械メーカー勤務／既婚・子ども3人）］</span></strong></p>
                                                    <p><strong><span style="font-size: 14pt;">中堅企業に営業として就職し、家族5人で仲良く暮らしていた安藤さん。しかし、会社の業績が悪化。年度が変わる度に給与が下がり、ボーナスもカット。そして、ついにリストラが！　ベテランの安藤さんもその対象になり、給与が年収ベースで150万円も減額に。</span><span style="font-size: 14pt;">加えて、同時期に上のお子様が大学へ進学、2番目のお子様も私立の高校へ進学、2年後には一番下のお子様の進学も控えており、出費はかさむばかり……。奥様もパートで家計を支えるものの、生活は苦しくなる一方です。カードローンで借入れをしても追いつかず、追いつかないどころか、カードローンの金利は膨れ上がって、ついには住宅ローンの返済も厳しくなってしまいました。このままではマイホームが「競売」にかけられてしまう……!?</span></strong></p>
                                                </div>
                                                <div style="height:20px" aria-hidden="true" class="wp-block-spacer"></div>
                                                <p>安藤さんには「家族みんなでこれからも、今までどおりここで暮らしたい！」という強い希望がありました。そこで「住宅ローンの借換で毎月の返済額を抑える」「自宅を残して借金を減らせる個人再生」「任意整理で金利負担を減らす」「リースバック」……etc.と、「住み続けること」を前提にいろんな視点から検討を始めました。</p>
                                                <p>結果、そのときの後藤さんが取り得る手段のなかで、「リースバック」（※固定資産に相当するものを他に売って、そこからリースするという金融取引のこと）が一番成功する可能性が高いと提案。また、カードローンの借入分に関しては「任意整理」の手続きをして、毎月の返済総額を減らすよう整理することに。</p>
                                                <p><strong>「銀行や役所に連絡を入れて手続きを進めると同時に、投資家にも紹介すると、まもなく1000万円で買い手が見つかり、後藤様は8万円の賃料を毎月支払って行くかたちで、そのまま住み続けることが叶いました。毎月の住居にかかる費用も抑えることができ、カードローンの借入分に関しても、毎月の返済総額が10万円から４万3000円にまで減らすことができました。『リースバック』と『任意整理』の“合わせ技”が成功した好例だと思います」（永井さん）</strong></p>
                                                <p>もちろん、どの案件もが前述のごとく、事がスムーズに運ぶとはかぎりません。万一、最悪のシナリオである「競売」を余儀なくされるケースにまで追い詰められてしまった場合は、「任意売却」という契約の方法があることを皆さん、ぜひ覚えておいてください。</p>
                                                <div style="height:20px" aria-hidden="true" class="wp-block-spacer"></div>
                                                <div style="height:20px" aria-hidden="true" class="wp-block-spacer"></div>
                                                <h2>「競売」が債務者に与えるあまりに大きなダメージ</h2>
                                                <figure class="wp-block-image size-large"><img src="assets/pixta_58681820_M.jpg" data-lazy-type="image" data-src="https://www.logrenove.jp/wp-content/uploads/2020/07/pixta_58681820_M.jpg" alt="" class="wp-image-4641 lazy-loaded" srcset="https://staging.logrenove.jp/wp-content/uploads/2020/07/pixta_58681820_M.jpg 730w, https://staging.logrenove.jp/wp-content/uploads/2020/07/pixta_58681820_M-350x233.jpg 350w, https://staging.logrenove.jp/wp-content/uploads/2020/07/pixta_58681820_M-159x106.jpg 159w" data-srcset="" sizes="(max-width: 730px) 100vw, 730px"><noscript><img src="https://www.logrenove.jp/wp-content/uploads/2020/07/pixta_58681820_M.jpg" alt="" class="wp-image-4641" srcset="https://staging.logrenove.jp/wp-content/uploads/2020/07/pixta_58681820_M.jpg 730w, https://staging.logrenove.jp/wp-content/uploads/2020/07/pixta_58681820_M-350x233.jpg 350w, https://staging.logrenove.jp/wp-content/uploads/2020/07/pixta_58681820_M-159x106.jpg 159w" sizes="(max-width: 730px) 100vw, 730px" /></noscript></figure>
                                                <p>さて。「任意売却」の詳細を紹介する前に、「もし、あなたのマイホームが競売にかかったらどうなるのか？」について、簡単にお話ししておきましょう。</p>
                                                <p>「競売」は、債権者と裁判所で手続きがどんどん進められ、債務者の介入する余地がほぼありません。『競売開始決定通知』が届くと、裁判所の執行官がやってきて、建物内外を写真撮影し、接道状況などの現場調査、近隣への聞き込みをします。</p>
                                                <p>「競売」の公告がされると、裁判所はその参加者を募るために物件情報・使用状況・諸権利関係などを広く一般公開します。同情報は裁判所で閲覧でき、不動産競売情報サイトにも掲載されるので、不特定多数の人や、近隣の住民にも知られてしまうというデメリットがあります。</p>
                                                <p>競売物件は、裁判所の執行官以外は物件の中に入ることができず内覧ができなかったり、契約不適合責任が免責だったり、落札者のリスクが大きい取引となるため、高値が付きません。市場価格の5～6割程度で落札されるケースが大半です。落札代金は住宅ローンの返済に充てられますので安価で落札されれば当然、債務が多く残ってしまいます。</p>
                                                <p>また、落札者が決まり代金が納付されると、所有権は落札者のものになります。落札者は裁判所に不動産引き渡し命令の申し立てをして執行日を設定します。それまでに債務者は引っ越しをしなければなりませんが、その費用が無かったとしても立ち退かない場合は強制執行され、追い出されてしまうのです。</p>
                                                <p>しかも、「競売」で残った債務は返済しなければいけませんが、そのほとんどは一括返済。残債額が大きく、支払うことができない場合は、自己破産をして免責にしてもらうしかありません。</p>
                                                <p>このように「競売」は、精神的にも金銭的にもダメージが大きく残ってしまいます。</p>
                                                <div style="height:20px" aria-hidden="true" class="wp-block-spacer"></div>
                                                <div style="height:20px" aria-hidden="true" class="wp-block-spacer"></div>
                                                <h2>「任意売却」最大のメリットは債務者の意思が反映できること</h2>
                                                <p>「任意売却」とは、こうした「競売」を回避するために、債権者との同意を得て市場で売却し、その代金で住宅ローンの返済を行う方法のことです。そのおおよその手順は、以下のような流れになります。</p>
                                                <p>まずは、不動産仲介業者を選定し、専任媒介契約を結びます。この契約によって、業者は債務者の代理人として債権者と任意売却について交渉ができるようになります。</p>
                                                <p>次に、債権者へ「任意売却」の申し入れをして、販売価格・販売期間・諸費用の負担……などの条件交渉をおこない、債権者から「任意売却」の同意を得て、一般市場で販売を行います。相場に近い金額での売却が可能となり、債権者も債権回収が目的ですから「競売」と比べ、高値で売却できることも。</p>
                                                <p>また、不動産売却にはさまざまな経費がかかりますが、住宅ローン返済が破綻している状態の人では持ち出しで用意することができないので、売却代金から控除してもらいます。その内容としては、不動産仲介手数料・抵当権抹消登記費用・滞納管理費や修繕積立金・後順位抵当権者に係る抵当権抹消応諾費用・差押債権者にかかる差押解除応諾費用……その他です。</p>
                                                <p>「その他」の項目の多くは引っ越し代。本来、引っ越し代は債権者や購入者に支払いの義務がありません。ただし、少なからずの債権者は「任意売却」をしようとしている売主は金銭的に厳しいだろうと承知しており、「住宅ローンも払えないほど困窮している人に引っ越し費用の捻出は難しいでしょう」と、“善意”でその費用を確保してもらえる可能性があります。認められた場合の相場は、10万～30万円といったところです。</p>
                                                <p>購入者が決まると売買契約を締結し、物件引き渡しなどの決済日を購入者とスケジュール調整します。引っ越しは打ち合わせた引渡し日までに完了させておきます。決済をして、売却代金を住宅ローンの返済に充てても債務は残りますが、返済方法を債権者と交渉できるため、少額（5000円～数万円）での分割返済も視野に入れることができるのです。</p>
                                                <p>このように「任意売却」は、「競売」と比べて強制的ではなく、債務者の意思が反映でき、精神的にも金銭的にもメリットのある方法だと言えます。<br><br>以上から「任意売却」のメリットとデメリットをまとめてみましょう。</p>
                                                <p><em><strong>［メリット］</strong></em><br><em><strong>・市場価格に近い金額で売却ができる</strong></em><br><em><strong>・債権者・購入者との交渉で債務者の意思が反映できる</strong></em><br><em><strong>・ご近所や周囲に事情を知られることなく売却ができる</strong></em><br><em><strong>・持ち出しがない</strong></em><br><em><strong>・引っ越し代を確保できる可能性がある</strong></em><br><em><strong>・残った債務は少額で分割返済が可能</strong></em></p>
                                                <p><em><strong>［デメリット］</strong></em><br><em><strong>・マイホームがなくなってしまう</strong></em><br><em><strong>・債権者の同意が必要</strong></em><br><em><strong>・信用情報機関に登録される</strong></em></p>
                                                <p><strong>「『任意売却』は『競売』を並行して進めているケースが多いので、『競売』の開札日までに『任意売却』の決済を完了しなければなりません。販売活動の時間もかぎられてしまいますから、それを成功させるためには早めの相談がポイントとなります」（永井さん）</strong></p>
                                                <div style="height:20px" aria-hidden="true" class="wp-block-spacer"></div>
                                                <div style="height:20px" aria-hidden="true" class="wp-block-spacer"></div>
                                                <h2>つい気持ちが大きくなってしまい無茶な住宅ローンを…</h2>
                                                <p>ウィズコロナの時代──『住宅ローン返済110番』は、今後ますます増加するであろうと予測される住宅ローン返済のトラブルに、より迅速なかたちで対応できるよう、YouTube・Facebook・Instagram……ほか、SNSでの展開を積極的に行っていく予定だと聞きます。</p>
                                                <p><strong>「相談者の置かれている現状や段階によって、リスケジュール・借り換え・個人再生・自己破産…などの債務整理、『任意売却』と『リースバック』、親子間・親族間売買…諸々と、場合によってはマイホームを残せる対処法もあります。住宅ローンの悩みは“病気”と同様、問題点の早期発見・対処が重要なのです」（永井さん）</strong></p>
                                                <p>最後に、「これから家を買う人たち」のために、『住宅ローン返済110番』から必読のアドバイスをいただきました。</p>
                                                <p><strong>「皆さまにとって、マイホーム購入は人生で一番大きな買い物になると思います。けれど、夢のマイホームだからといって、つい気持ちが大きくなってしまい、無茶な住宅ローンを組むことは厳禁！　新築物件では設備や外構に、中古物件ではリフォームにこだわり過ぎて思った以上にお金がかかってしまったり、ひとりでの組み立てができず、夫婦ふたりで住宅ローンの組み立てをすることになったり、ボーナス併用払いを利用したり、貯蓄がなく自己資金もないからと諸経費も含めてフルローンを組んだりするのは、ローン破綻のリスクが高く、返済困難の原因になります」（永井さん）</strong></p>
                                                <p>冒頭でも申したとおり、病気やケガ・離婚・ボーナスカット・リストラ……と、従来の収入が見込めなくなり、住宅ローンの返済が困難になる可能性は誰にでも起こり得ること。年齢を重ねるにつれて、子どもが通う学習塾・高校・大学の学費や、親の介護といった将来的な出費で返済が圧迫されることも十分に考えられ、これらにまだ先行きが見えない新型コロナウイルスの影響も加わって、私たち個人の経済事情がいつどうなるかは、なかなか想像がつきません。</p>
                                                <p>そんなときにマイホームを手放さなくても済むように、収入が減ってしまう、あるいは家計の支出が増えてしまうリスクも想定し、住宅ローンとライフプランは、ゆとりのある組み立てを心がけるべきでしょう。</p>
