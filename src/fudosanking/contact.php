    (function($) {
        var PREF_MAP = [
            null, '北海道', '青森県', '岩手県', '宮城県',
            '秋田県', '山形県', '福島県', '茨城県', '栃木県',
            '群馬県', '埼玉県', '千葉県', '東京都', '神奈川県',
            '新潟県', '富山県', '石川県', '福井県', '山梨県',
            '長野県', '岐阜県', '静岡県', '愛知県', '三重県',
            '滋賀県', '京都府', '大阪府', '兵庫県', '奈良県',
            '和歌山県', '鳥取県', '島根県', '岡山県', '広島県',
            '山口県', '徳島県', '香川県', '愛媛県', '高知県',
            '福岡県', '佐賀県', '長崎県', '熊本県', '大分県',
            '宮崎県', '鹿児島県', '沖縄県'
        ];

        /// init address field
        (function($) {

            function AddressSelector($fields) {
                this.$fields = $($fields);

                this.initialize_address_selector();
            }
            AddressSelector.prototype = {
                initialize_address_selector: function() {
                    var self = this;
                    this.$selects = this.$fields.find('.pref, .city, .ooaza, .tyoume');

                    this.$selects.slice(0, -1)
                        .prop('disabled', true)
                        .on('change', function() {
                            self.on_change(this);
                        });
                    this.update_select_list(0, PREF_MAP);
                },

                update_select_list: function(index, choices) {
                    var $target = this.$selects.eq(index);

                    $target.find('option:not(:first)').remove();
                    $target.prop('disabled', false);
                    _.each(choices, function(val) {
                        if (val !== '' && !val)
                            return;
                        var text = val;
                        if (!val) {
                            val = ' ';
                            text = '(なし)';
                        }
                        $('<option>').val(val).text(text).appendTo($target);
                    });

                    this.$selects.slice(index + 1).each(function() {
                        $(this)
                            .prop('disabled', true)
                            .find('option:not(:first)').remove();
                    });
                },

                on_change: function(changed) {
                    var self = this;
                    var index = this.$selects.index(changed);
                    var locations = this.$selects.slice(0, index + 1).map(function() { return encodeURIComponent($(this).val()); }).get();

                    if (!$(changed).val()) {
                        // 最初のcellの場合はクリアして終わり
                        self.update_select_list(index + 1, []);
                        this.$selects.eq(index + 1).prop('disabled', true);
                        return;
                    }

                    $.ajax('/data/area/' + locations.join('/'))
                        .done(function(data) {
                            self.update_select_list(index + 1, data.areas);
                            var $target = self.$selects.eq(index + 1);
                            $target.trigger('updated');

                            if (data.areas.length == 1)
                                $target.val(data.areas[0]).trigger('change');
                        })
                        .fail(function() {
                            // TODO
                        });
                },

                set_address: function(pref, city, ooaza, tyoume) {
                    function select_val($node, val) {
                        return $node.val(val).trigger('change');
                    }
                    var self = this;
                    var $pref = self.$selects.eq(0),
                        $city = self.$selects.eq(1),
                        $ooaza = self.$selects.eq(2),
                        $tyoume = self.$selects.eq(3);

                    if (!pref)
                        return;
                    select_val($pref, pref);

                    if (!city)
                        return;

                    $city.one('updated', function() {
                        select_val($city, city);
                        if (!ooaza)
                            return;

                        $ooaza.one('updated', function() {
                            select_val($ooaza, ooaza);

                            if (tyoume === undefined)
                                return;

                            $tyoume.one('updated', function() {
                                setTimeout(function() {
                                    select_val($tyoume, tyoume);
                                }, 0);
                            });
                        });
                    });
                }
            };

            $.fn.address_editor = function(initial) {
                var $this = $(this);
                if ($this.length === 0)
                    return;
                var editor = $this.data('address_editor');
                if (!editor) {
                    editor = new AddressSelector($this);
                    $this.data('address_editor', editor);

                    if (initial)
                        editor.set_address(initial.pref, initial.city, initial.ooaza, initial.tyoume);
                }

                return editor;
            };
        })(jQuery);


        _.delay(function() {
            $('.address-fields').address_editor({ pref: '', city: '', ooaza: '', tyoume: '' });

            // 住所が設定されている選択肢を選ぶ
            _.each([
                ['None', 'pref'],
                ['None', 'city'],
                ['None', 'ooaza'],
                ['None', 'tyoume']
            ], function(val, i) {
                if (val[0]) {
                    _.delay(function() {
                        var $select = $('select[name="address_' + val[1] + '"]');
                        $select.val(val[0]);
                        $select.trigger('change');
                    }, 150 * i);
                }
            });
        });

    })(jQuery);
    </script>
    
    
