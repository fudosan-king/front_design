  <h2>敷金（保証金）</h2>
  <div class="explanation-entry">
    敷金（保証金）とは退去する際、入居中に破損させてしまった部屋の設備・内装を修理したり、退去時にハウスクリーニングをするための料金の事です。それを契約時に先払いしているのです。<br>
    なぜ先払いかというと、退室の際、料金を支払えないとなると、前の方が使用されてたそのままの状態で入居者を募集しなければならなくなり、次の居住希望者が現れにくくなります。ですから、敷金はお部屋の修理・修繕後、残代金を返還されます。<br>
    敷金なしという物件でも、結局設備を破損させていたなら請求される料金なので、敷金なしの物件が安いと単純に考えてはいけません。<br>
    通常家賃の0カ月～2カ月 </div>
  <h2>礼金</h2>
  <div class="explanation-entry">
    礼金とはお部屋を賃借する際にオーナーにお礼として支払うお金になります。<br>
    お部屋を借りられる方が「返還されるお金、返還されないお金」と耳にすることがありますが、礼金が返還されないお金になります。通常家賃の0カ月～2カ月 </div>
  <h2>仲介手数料</h2>
  <div class="explanation-entry">
    オーナーとお客様をを仲介した際に、その報酬として受け取る手数料のことです。<br>
    礼金と同等に考えがちですが、礼金はオーナー様、仲介手数料は不動産会社への支払になります。賃貸契約の場合、管理費・共益費を除いた月額家賃の1か月分になります。 </div>
  <h2>保証料</h2>
  <div class="explanation-entry">
    賃貸住宅契約時、賃借人の保証人を代行する会社に支払う料金になります。<br>
    全ての物件に該当するわけではありませんが、近年保証会社加入必須物件が増えてきています。料金形態は賃料の30％～100％と、保証会社によって異なります。 </div>
