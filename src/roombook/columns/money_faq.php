  <h2>敷金・礼金が上乗せされているケース</h2>
  <div class="explanation-entry">
通常の家賃に敷金・礼金を上乗せし、初期費用を安く抑えることができます。初期費用は抑えることが可能ですが、その分を分割でお支払しているケースです。 </div>
  <h2>別の名目で実は請求されているケース</h2>
  <div class="explanation-entry">
敷金や礼金がゼロの代わりに他の必須加入項目が多かったりしたことありませんか。初期費用を抑えようと思ったのに請求書の欄には見慣れない項目が多かったりします。結果、初期費用が安い物件でも色々なケースが存在します。その都度不動産会社に問い合わせるのが一番間違えないお部屋探しになります。 </div>
  <h2>賃料交渉のテクニック</h2>
  <div class="explanation-entry">
適正賃料を知り、借りる意思をはっきり伝える<br>
    とりあえず「安くして！」と言うお客様もいらっしゃいますが、元々相場に比べ賃料が安価な物件を、オーナーに更に値引きして貰うのは大変難しい話です。逆に、物件賃料が相場に比べ高額だった場合、少々値引きして貰ったところで本当の値引きにはなっていません。「適正賃料を知ること」つまり「相場を知ること」ですが、一般のお客様には難しい話です。
    これはその地元に詳しい、信頼できるスタッフとよく吟味することが大事です。
