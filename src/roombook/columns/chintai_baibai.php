<h1>お部屋を借りるのと買うのとはどっちがお得? (賃貸 vs 持ち家)</h1>
<div class="entry-body">
  <h2>賃貸物件に住むことのメリット</h2>
  <div class="explanation-entry">
    <img src="../images/merit.png" width="130" height="93">
    <ul>
      <li>ライフスタイルに合わせて自由に住み替える事ができる。</li>
      <li>長期間の住宅ローンを組まないので、精神的な負担がない。</li>
      <li>不動産を購入することで毎年課税される、固定資産税・都市計画税の税負担がない。</li>
      <li>不動産購入時にかかる不動産取得税のように、初期費用としての課税制度がない。</li>
      <li>不動産相場の変動が気にならない。</li>
    </ul>
  </div>
    
  <h2>賃貸物件に住むことのデメリット</h2>
  <div class="explanation-entry">
    <img src="../images/demerit.png" width="130" height="93">
    <ul>
      <li>家賃を払い続けても自分のものにならない。</li>
      <li>定期的に更新料がかかる。(2年更新が一般的)</li>
      <li>自分のものでないのでリフォームはできない。</li>
      <li>高齢になると、自分の希望する賃貸に住むことが難しくなる。</li>
    </ul>
    賃貸の場合、メリットとしては自分のライフスタイルに合わせて自由に住み替えができることや、長期間支払わないといけない住宅ローンのプレッシャーがない分、精神的に楽な点などが挙げられます。お子様が生まれたり、両親同居等の家族構成の変化にも気軽に対応することができます。ただ、理想の物件を探すのは非常に困難な事ですし、老後の住まいが確保できない不安も出てきます。
  </div>
  <h2>こんな方は賃貸がおススメ</h2>
  <div class="explanation-entry">
    <ul>
      <li>いろんなエリアに住んでみたい方</li>
      <li>常に新しい物件に住みたい方</li>
      <li>様々なライフスタイルを楽しみたい方</li>
    </ul>
    <p>例えば...新婚さん/独身の方/転勤の多い方</p>
  </div>
  
  <h2>持ち家のメリット</h2>
  <div class="explanation-entry"><img src="../images/merit.png" width="130" height="93">
    <ul>
      <li>自己資産になる。</li>
      <li>物件のクオリティが高い。</li>
      <li>住宅ローンを終える前提で、老後に安心感がある。</li>
      <li>自由に住宅設備、室内をリフォームする事ができる。</li>
    </ul>
  </div>
  <h2>持ち家のデメリット</h2>
  <div class="explanation-entry"><img src="../images/demerit.png" width="130" height="93">
    <ul>
      <li>住宅ローンを負担し続けなければならない。</li>
      <li>気軽に引越することができない。</li>
      <li>税負担が多い。</li>
      <li>住宅設備等のメンテナンスで、予想外の出費が必要になることもある。</li>
    </ul>
    ご自身で固有資産を持つことが最大のメリットになります。<br>
    ローンが完済すれば、多少の維持費だけの支払いになり、生活にゆとりが出るでしょう。また、賃貸マンションに比べ、分譲マンションは建設物としてのクオリティは高いといえます。 <br>
